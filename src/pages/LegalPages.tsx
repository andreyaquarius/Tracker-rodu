import type { ReactNode } from "react";
import { legalConfig } from "../config/legal";

type LegalPageKind = "privacy" | "terms";

function LegalPublicNav({ current }: { current: LegalPageKind }) {
  const items = [
    { href: "/", label: "Головна", key: "home" },
    { href: "/features", label: "Можливості", key: "features" },
    { href: "/pricing", label: "Тарифи", key: "pricing" },
    { href: "/privacy", label: "Політика конфіденційності", key: "privacy" },
    { href: "/terms", label: "Умови користування", key: "terms" },
  ];

  return (
    <nav className="public-nav" aria-label="Публічна навігація">
      {items.map((item) => (
        <a
          aria-current={item.key === current ? "page" : undefined}
          href={item.href}
          key={item.key}
        >
          {item.label}
        </a>
      ))}
    </nav>
  );
}

function ContactNotice({ purpose }: { purpose: "privacy" | "support" }) {
  const email = purpose === "privacy"
    ? legalConfig.contacts.privacyEmail
    : legalConfig.contacts.supportEmail;

  if (email) {
    return <a href={`mailto:${email}`}>{email}</a>;
  }

  return (
    <span>
      контактну адресу буде опубліковано після підтвердження реквізитів
      власником сервісу
    </span>
  );
}

function LegalLayout({
  kind,
  title,
  description,
  children,
}: {
  kind: LegalPageKind;
  title: string;
  description: string;
  children: ReactNode;
}) {
  const currentPath = kind === "privacy" ? "/privacy" : "/terms";

  return (
    <main className="legal-page">
      <header className="legal-hero">
        <a className="brand legal-brand" href="/" aria-label="Перейти на головну">
          <span className="brand-mark">
            <img src="/tracker-rodu-logo.png" alt="" />
          </span>
          <span>
            <strong>{legalConfig.serviceName}</strong>
            <small>{legalConfig.brandName}</small>
          </span>
        </a>
        <LegalPublicNav current={kind} />
        <span className="eyebrow">
          {kind === "privacy" ? "Приватність" : "Умови користування"}
        </span>
        <h1>{title}</h1>
        <p>{description}</p>
        <div className="legal-meta">
          <span>Редакція: {legalConfig.currentRevision.label}</span>
          <a href={currentPath}>Поточна редакція</a>
        </div>
      </header>
      <article className="legal-card">{children}</article>
      <footer className="legal-footer">
        <a href="/">На головну</a>
        <a href="/features">Можливості</a>
        <a href="/pricing">Тарифи</a>
        <a href="/privacy">Політика конфіденційності</a>
        <a href="/terms">Умови користування</a>
      </footer>
    </main>
  );
}

