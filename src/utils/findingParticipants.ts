export function participantRoles(findingType: string): string[] {
  return rolesByType[findingType] ?? rolesByType.інше;
}

export function participantSummary(participants: FindingParticipant[]): string {
  return participants
    .filter((participant) => participant.name.trim())
    .map((participant) => `${participant.role}: ${participant.name}`)
    .join("; ");
}

export function primaryParticipantName(participants: FindingParticipant[]): string {
  return participants.find((participant) => participant.name.trim())?.name ?? "";
}
